# =============================================================================
# 테이블 이름 상수
# =============================================================================
# Supabase 쿼리 시 사용하는 테이블명
# 오타 방지를 위해 상수로 관리
# =============================================================================

TABLE_USERS = "users"
TABLE_CATEGORIES = "categories"
TABLE_PAYMENT_METHODS = "payment_methods"
TABLE_RECEIPTS = "receipts"


# =============================================================================
# users 테이블
# =============================================================================
# 사용자 정보
#
# 컬럼:
#   - id (PK): 기본키, 자동 생성
#   - name: 사용자 이름
#   - user_id (UQ): 로그인 ID, 유니크
#   - password: 비밀번호 (해시 저장 권장)
#   - tel: 전화번호
#   - created_at: 생성일시
# =============================================================================


# =============================================================================
# categories 테이블
# =============================================================================
# 영수증 카테고리 (식비, 교통비, 쇼핑 등)
#
# 컬럼:
#   - id (PK): 기본키, 자동 생성
#   - name (UQ): 카테고리명, 유니크
#   - icon: 아이콘 (이모지 또는 아이콘명)
# =============================================================================


# =============================================================================
# payment_methods 테이블
# =============================================================================
# 결제 수단 (현금, 카드, 계좌이체 등)
#
# 컬럼:
#   - id (PK): 기본키, 자동 생성
#   - name (UQ): 결제수단명, 유니크
# =============================================================================


# =============================================================================
# receipts 테이블
# =============================================================================
# 영수증 데이터 (핵심 테이블)
#
# 컬럼:
#   - id (PK): 기본키, 자동 생성
#   - user_id (FK): users.id 참조
#   - category_id (FK): categories.id 참조
#   - payment_method_id (FK): payment_methods.id 참조
#   - date: 결제 날짜
#   - total_amount: 총 금액
#   - store_name: 상호명
#   - image_path: 영수증 이미지 경로
#   - details (JSONB): 상세 항목 (품목, 수량, 단가 등)
#   - created_at: 생성일시
#
# details 예시:
#   [
#       {"name": "아메리카노", "quantity": 2, "price": 4500},
#       {"name": "케이크", "quantity": 1, "price": 6000}
#   ]
# =============================================================================